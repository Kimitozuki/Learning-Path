public class Java_13_Operator_Assingment {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
         
        //Operator Assingment
        
        //Assingement Penjumlahan
        int a = 50;
        a += 10; //sama aja a = a + 10
        System.out.println("Nilai a = " + a);
        
        //Assingement Pengurangan
        int b = 50;
        b -= 5; 
        System.out.println("Nilai b = " + b);
        
        //Assingment Perkalian
        int c = 50;
        c *= 2; //sama aja c = c * 2
        System.out.println("Nilai c = " + c);
        
        //Assingment Pembagian
        int d = 50;
        c /= 2; //sama aja d = d / 2
        System.out.println("Nilai d = " + d);
        
        //Assingment Modulus
        int e = 50;
        e %= 30;
        System.out.printf("Nilai e = %d \n",e);
                      
    }
}
